	<h2>Создание SMS события</h2>

	<p>Модуль позволяет создать SMS событие на основе любого почтового события в системе.</p>
	<p>Работа с SMS событиями точно такая же, как и работа со стандартными почтовыми событиями.</p>

	<p><b>Форма создания SMS события</b></p>
	<p><b>Доступные макросы</b> Список полей, которые можно использовать при создании SMS события.</p>

	<p><b>Телефон (макрос или номер)</b>В этом поле можно указать либо номер телефона, либо макрос(из доступных макросов), в котором он хранится.</p>
	<p>Так же можно указать макрос с ID или EMAIL пользователя(пример чуть ниже). В этом случае будет осуществлен поиск мобильного телефона в следующем порядке:</p>
	<ul>
		<li>Телефон (PERSONAL_PHONE)</li>
		<li>Мобильный (PERSONAL_MOBILE)</li>
		<li>Рабочий (WORK_PHONE)</li>
	</ul>

	<p><b>Введите текст SMS,которое будет отправлено пользователю</b> Введите шаблон SMS. Можно использовать «Доступные макросы».Это SMS будет отправлено на номер, который указан в поле «Телефон».</p>

	<p><b>Введите текст SMS, которое будет отправлено администратору(-рам) сайта</b> Введите  шаблон SMS. Можно использовать «Доступные макросы».</p>

	<p>Телефон(ы)  администратора(-ов) нужно задать в настройках модуля на вкладке «Настройки для сайта» </p>
